/* TIPOS DE DATOS */
/* 
    TIPOS DE DATOS PRIMITIVOS
        -> Aquel al cual se puede acceder a su valor de 
        manera directa.
            -> string (cadena de texto) -> "hola", 'hola'
            -> number (numero) -> 32, -32, 3.2, -3.2
            -> boolean (valores binarios) -> true/false
            -> null -> Un valor vacio definido a drede
            -> undefined -> Un valor vacio por defecto

    TIPOS DE DATOS COMPUESTOS
        -> Aquel al cual se accede a sus valores por referencia.
            -> object (coleccion de variables privadas referenciadas por nombre de variable.)
*/


